# Функциональный дизайн (англ. Functional design) — основной шаблон
# проектирования, использующийся для упрощения проектирования ПО.
# Функциональный дизайн гарантирует, что каждый модуль компьютерной программы
# имеет только одну обязанность и исполняет её с минимумом побочных эффектов
# на другие части программы. Функционально разработанные модули имеют низкое
# зацепление.
