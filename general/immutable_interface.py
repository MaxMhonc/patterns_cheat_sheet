# В объектно-ориентированном программировании, «неизменяемый Интерфейс» — это
# шаблон для проектирования неизменяемого объекта.[1][2][3] Неизменяемый
# интерфейс включает в себя определение типа, который не предоставляет никаких
# методов, которые могут изменить состояние. Объекты, на которые ссылается
# этот тип, выглядят как неизменяемые.
